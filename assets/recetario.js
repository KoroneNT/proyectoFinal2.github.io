function buscarReceta() {
    const receta = document.getElementById('receta').value.trim().toLowerCase();
    let recetaBuscada;
    let ingredientes;
    let preparacion;
    for (let i = 0; i < recetas.length; i++) {
        if (recetas[i].receta.trim().toLowerCase() === receta) {
            recetaBuscada = recetas[i].receta;
            ingredientes = recetas[i].ingredientes;
            preparacion = recetas[i].preparacion;
            break;
        }
    }
    if (recetaBuscada) {
        document.getElementById('title').innerText = "Receta " + recetaBuscada;
        const ingredientesHTML = ingredientes.map(ingrediente => `<li>${ingrediente}</li>`).join('');
        document.getElementById('ingredientes').innerHTML = "Ingredientes:<br><ul>" + ingredientesHTML + "</ul>";
        const lista = document.getElementById('preparacion');
        lista.innerHTML = ""; // limpiar la lista anterior
        const ul = document.createElement('ul');
        const preparacionArray = preparacion.split('. ');
        for (let i = 0; i < preparacionArray.length; i++) {
            const li = document.createElement('li');
            li.textContent = preparacionArray[i];
            ul.appendChild(li);
        }
        lista.appendChild(ul);
    } else {
        document.getElementById('title').innerText = "No se encontró la receta";
        document.getElementById('ingredientes').innerHTML = "";
        document.getElementById('preparacion').innerHTML = "";
    }
}